//AccessArrayElementUsingIndex
class main{
	public static void main(String[] args){
		//declare+initializing an array
		int[] arr={1,2,3};
		//access each elements
		System.out.println("Accessing each element");
		System.out.println("1st element:"+arr[0]);
		System.out.println("2nd element:"+arr[1]);
		System.out.println("3rd element:"+arr[2]);
	}
}
